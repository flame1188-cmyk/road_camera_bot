# Предвычисленные данные для быстрого поиска
_precomputed: list[tuple[float, float, str, str, float]] = []


def _precompute() -> None:
    """Предвычисляет синусы/косинусы для быстрого поиска."""
    global _precomputed
    if _precomputed:
        return
    for lat, lon, code, name in _REGION_CENTERS:
        _precomputed.append((
            math.radians(lat), math.radians(lon), code, name,
        ))


def find_region_by_coords(lat: float, lon: float) -> tuple[str, str] | None:
    """Определяет код региона GIBDD по координатам.

    Использует Haversine-расстояние до ближайшего центра региона.

    Returns:
        Tuple (gibdd_code, region_name) или None если координаты вне РФ.
    """
    _precompute()

    best_dist = float("inf")
    best_code = None
    best_name = None

    lat_r = math.radians(lat)
    lon_r = math.radians(lon)

    for rlat_r, rlon_r, code, name in _precomputed:
        dlat = lat_r - rlat_r
        dlon = lon_r - rlon_r
        a = (math.sin(dlat / 2) ** 2
             + math.cos(rlat_r) * math.cos(lat_r) * math.sin(dlon / 2) ** 2)
        dist = 2 * math.asin(math.sqrt(min(a, 1.0)))

        if dist < best_dist:
            best_dist = dist
            best_code = code
            best_name = name

    # Проверяем что координаты в пределах РФ (~400 км от ближайшего центра)
    if best_dist > 3.5:
        logger.warning(
            f"Координаты {lat}, {lon} — за пределами РФ "
            f"(ближайший: {best_name}, {best_dist * 6371:.0f} км)"
        )
        return None

    logger.info(
        f"Регион по координатам {lat}, {lon}: "
        f"{best_name} (код {best_code}, ~{best_dist * 6371:.0f} км)"
    )
    return (best_code, best_name)
